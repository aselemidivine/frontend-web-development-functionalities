// 1. Number: Represents both integer and floating-point numbers.
let age = 25; // this person is 25 years old
let price = 19.99;


let fame = ""; // null 
let tame; // name is not defined


// 2. String: Represents a sequence of characters enclosed in single ('') or double ("") quotes.
let name = "Aselemi Divine";  // this is the name of the instructor
let message = 'Hello, world!';

// 3. Boolean: Represents true or false values.
let isLogged = true;
let isApproved = false;

// 4. Undefined: Represents a variable that has been declared but not assigned a value.
let username;
console.log(username); // Output: undefined

// 5. Null: Represents the intentional absence of any value.
let data = null;

// 6. Object: Represents a collection of key-value pairs enclosed in curly braces {}.
let person = {
    name: "Alice",
    age: 30,
    isAdmin: false
  };
  
// [] = an array 
// {} = an object
// "" = a string 

//  7. Array: Represents a list of values enclosed in square brackets [].
  let colors = ["red", "green", "blue"];


//   8. Symbol: Represents a unique and immutable value often used as object property keys.
const id = Symbol("id");


// 9. Function: Represents a block of code that can be executed when called.
  function greet(name) {
    return "Hello, " + name + "!";
  }
  

